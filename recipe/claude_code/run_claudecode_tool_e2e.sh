#!/usr/bin/env bash
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd "${SCRIPT_DIR}/../.." && pwd)"

XTUNER_USE_LMDEPLOY="${XTUNER_USE_LMDEPLOY:-1}"
XTUNER_CLAUDECODE_TOOL_MAX_TURNS="${XTUNER_CLAUDECODE_TOOL_MAX_TURNS:-4}"
XTUNER_CLAUDECODE_TOOL_MAX_TOKENS="${XTUNER_CLAUDECODE_TOOL_MAX_TOKENS:-1024}"
XTUNER_CLAUDECODE_TOOL_CONTEXT_LENGTH="${XTUNER_CLAUDECODE_TOOL_CONTEXT_LENGTH:-32768}"
XTUNER_CLAUDECODE_TOOL_TIMEOUT_S="${XTUNER_CLAUDECODE_TOOL_TIMEOUT_S:-600}"
XTUNER_CLAUDECODE_TOOL_OUTPUT_DIR="${XTUNER_CLAUDECODE_TOOL_OUTPUT_DIR:-/tmp/xtuner_claudecode_tool_e2e}"
XTUNER_CLAUDECODE_TOOL_CALL_PARSER="${XTUNER_CLAUDECODE_TOOL_CALL_PARSER:-qwen3p5}"
XTUNER_CLAUDECODE_REASONING_PARSER="${XTUNER_CLAUDECODE_REASONING_PARSER:-qwen3}"

if [[ -z "${ROLLOUT_MODEL_PATH:-}" ]]; then
  echo "ROLLOUT_MODEL_PATH must be set." >&2
  exit 1
fi

if [[ ! -e "${ROLLOUT_MODEL_PATH}" ]]; then
  echo "ROLLOUT_MODEL_PATH does not exist: ${ROLLOUT_MODEL_PATH}" >&2
  exit 1
fi

mkdir -p "${XTUNER_CLAUDECODE_TOOL_OUTPUT_DIR}"

export PYTHONPATH="${REPO_ROOT}:${PYTHONPATH:-}"

export ROLLOUT_MODEL_PATH
export XTUNER_USE_LMDEPLOY
export XTUNER_CLAUDECODE_TOOL_MAX_TURNS
export XTUNER_CLAUDECODE_TOOL_MAX_TOKENS
export XTUNER_CLAUDECODE_TOOL_CONTEXT_LENGTH
export XTUNER_CLAUDECODE_TOOL_TIMEOUT_S
export XTUNER_CLAUDECODE_TOOL_OUTPUT_DIR
export XTUNER_CLAUDECODE_TOOL_CALL_PARSER
export XTUNER_CLAUDECODE_REASONING_PARSER

cd "${REPO_ROOT}"

echo "Running Claude Code calculator tool E2E"
echo "  repo root: ${REPO_ROOT}"
echo "  model: ${ROLLOUT_MODEL_PATH}"
echo "  output: ${XTUNER_CLAUDECODE_TOOL_OUTPUT_DIR}"
echo "  max_turns: ${XTUNER_CLAUDECODE_TOOL_MAX_TURNS}"
echo "  max_tokens: ${XTUNER_CLAUDECODE_TOOL_MAX_TOKENS}"
echo "  context_length: ${XTUNER_CLAUDECODE_TOOL_CONTEXT_LENGTH}"
echo "  tool_call_parser: ${XTUNER_CLAUDECODE_TOOL_CALL_PARSER}"
echo "  reasoning_parser: ${XTUNER_CLAUDECODE_REASONING_PARSER}"

python "${SCRIPT_DIR}/test_claude_code_with_calculator.py"
